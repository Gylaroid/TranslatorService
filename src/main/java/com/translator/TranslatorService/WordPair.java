package com.translator.TranslatorService;


public class WordPair {
    private String en;
    private String ru;

    public String getEn(){
        return this.en;
    }

    public String getRu(){
        return this.ru;
    }

    public void setEn(String word){
        this.en = word;
    }

    public void setRu(String word){
        this.ru = word;
    }
}
